package JavaMatemática;

public class áreacubo {
    public static void main(String[] args) {
        int a = 7;
        double área = 6 * a * a;
        double volume = a * a * a;

        System.out.println("A área do cubo é: " + área);
        System.out.println("O volume do cubo é: " + volume);
    }
}
