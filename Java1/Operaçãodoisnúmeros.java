
public class Operaçãodoisnúmeros {
    public static void main(String[] args) {
        int num1 = 2; // Cria a variável de um número 
        int num2 = 45; // Cria a variável de outro número 

        int Soma = num1 + num2; // Faz a soma das variáveis
        int Diferença = num1 - num2; // Faz a subtração das variáveis
        int Quociente = num1 / num2; // Faz a divisão das variáveis
        int Produto = num1 * num2; // Faz a multiplicação das variáveis 

        System.out.println("Soma: " + Soma); // Imprime os resultados
        System.out.println("Diferença: " + Diferença);
        System.out.println("Produto: " + Produto);
        System.out.println("Quociente: " + Quociente);
    }
}
